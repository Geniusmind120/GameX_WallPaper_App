package com.satyajit.gamex.GetterSetter;

public class RecentItems {
    private String name,image,count;  //Our Members


    public RecentItems(String name,String image,String count) {
        this.name = name;
        this.image = image;
        this.count = count;
    }





    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }

    public String getCount() {
        return count;
    }





}